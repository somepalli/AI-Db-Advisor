# Global workflow instances (keyed by ds_id)
_workflows: Dict[str, ApprovalWorkflow] = {}


def get_workflow(ds_id: str) -> ApprovalWorkflow:
    """
    Get or create approval workflow for a datasource.

    Args:
        ds_id: Datasource identifier

    Returns:
        ApprovalWorkflow instance
    """
    if ds_id not in _workflows:
        _workflows[ds_id] = ApprovalWorkflow(ds_id)

    return _workflows[ds_id]
